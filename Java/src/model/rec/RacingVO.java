package model.rec;

public class RacingVO {
	private int racing_code;
	private int winner_code;
	private int racing_bet_code;
	
	public int getRacing_code() {
		return racing_code;
	}
	public void setRacing_code(int racing_code) {
		this.racing_code = racing_code;
	}
	public int getWinner_code() {
		return winner_code;
	}
	public void setWinner_code(int winner_code) {
		this.winner_code = winner_code;
	}
	public int getRacing_bet_code() {
		return racing_bet_code;
	}
	public void setRacing_bet_code(int racing_bet_code) {
		this.racing_bet_code = racing_bet_code;
	}


}
