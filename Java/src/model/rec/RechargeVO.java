package model.rec;

public class RechargeVO {
	private int		rc_code;
	private int		rc_price;
	private String 	cust_id;
	public int getRc_code() {
		return rc_code;
	}
	public void setRc_code(int rc_code) {
		this.rc_code = rc_code;
	}
	public int getRc_price() {
		return rc_price;
	}
	public void setRc_price(int rc_price) {
		this.rc_price = rc_price;
	}
	public String getCust_id() {
		return cust_id;
	}
	public void setCust_id(String cust_id) {
		this.cust_id = cust_id;
	}
	
	
	
}
